print("=== PYTHON FOR ALGEBRAISTS - FOUNDATIONS ===")
print("Lesson 1: Variables and Mathematical Operations")

# Basic variables - like algebraic definitions
x = 5
y = 3

print(f"\nOur variables: x = {x}, y = {y}")

# Mathematical operations
print("\n--- Basic Operations ---")
print(f"Addition: {x} + {y} = {x + y}")
print(f"Subtraction: {x} - {y} = {x - y}")
print(f"Multiplication: {x} × {y} = {x * y}")
print(f"Division: {x} ÷ {y} = {x / y}")
print(f"Exponent: {x}² = {x ** 2}")

# Algebraic expression
print("\n--- Algebraic Expression ---")
# Calculate: 2x + 3y
result = 2*x + 3*y
print(f"2x + 3y where x={x}, y={y} = {result}")

print("\n✅ Foundation setup complete! Ready for next lesson.")